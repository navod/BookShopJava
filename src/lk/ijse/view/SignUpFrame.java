    public String getGender() {
        String gen = "";
        if (rMale.isSelected()) {

            gen = "Male";
        } else if (rFemale.isSelected()) {

            gen = "Female";
        }
        return gen;
    }

    public String getRole() {
        String role = "";
        if (rCashier.isSelected()) {
            role = "Cashier";
        } else if (rOwner.isSelected()) {
            role = "Owner";
        }
        return role;
    }

