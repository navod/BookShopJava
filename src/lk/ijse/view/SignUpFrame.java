    private void loadUserId() throws ClassNotFoundException, SQLException {
        String id = new LoginController().getLastId();
        if (id == null) {
            txtUid.setText("R0001");
        } else {
            String split[] = id.split("R");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                txtUid.setText("R000" + num);
            } else if (num < 100) {
                txtUid.setText("R00" + num);
            } else if (num < 1000) {
                txtUid.setText("R0" + num);
            } else {
                txtUid.setText("R" + num);
            }
        }
    }
