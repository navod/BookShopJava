    private void loadInvoiceId() throws ClassNotFoundException, SQLException {
        String invoiceId = new InvoiceController().getLastId();
        if (invoiceId == null) {
            lblInvoice.setText("ID0000001");
        } else {
            String split[] = invoiceId.split("ID");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                lblInvoice.setText("ID000000" + num);
            } else if (num < 100) {
                lblInvoice.setText("ID00000" + num);
            } else if (num < 1000) {
                lblInvoice.setText("ID0000" + num);
            } else if (num < 10000) {
                lblInvoice.setText("ID000" + num);
            } else if (num < 100000) {
                lblInvoice.setText("ID00" + num);
            } else if (num < 1000000) {
                lblInvoice.setText("ID0" + num);
            } else if (num < 1000000) {
                lblInvoice.setText("ID" + num);
            }
        }
    }

