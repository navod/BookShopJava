    private void txtTotalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTotalKeyReleased
        try {
            for (CashDiscount range1 : new DiscountController().getRange1()) {
                double cash1 = range1.getCash();
                double discount1 = range1.getDiscount();

                for (CashDiscount range2 : new DiscountController().getRange2()) {
                    double cash2 = range2.getCash();
                    double discount2 = range2.getDiscount();

                    for (CashDiscount range3 : new DiscountController().getRange3()) {
                        double cash3 = range3.getCash();
                        double discount3 = range3.getDiscount();

                        String cardDiscount = new DiscountController().getCardDiscount();
                        txtCardDiscount.setText(cardDiscount);

                        double total = Double.parseDouble(txtTotal.getText());
                        if (total >= cash1 && total < cash2) {
                            double aRD1 = total * discount1 / 100;
                            txtARDiscount.setText(Double.toString(aRD1));
                            double netTotal = total - aRD1;
                            txtNetTotal.setText(Double.toString(netTotal));
                        } else if (total >= cash2 && total < cash3) {
                            double aRD2 = total * discount2 / 100;
                            txtARDiscount.setText(Double.toString(aRD2));
                            double netTotal2 = total - aRD2;
                            txtNetTotal.setText(Double.toString(netTotal2));
                        } else if (total > cash3) {
                            double aRD3 = total * discount3 / 100;
                            txtARDiscount.setText(Double.toString(aRD3));
                            double netTotal3 = total - aRD3;
                            txtNetTotal.setText(Double.toString(netTotal3));
                        } else {
                            txtNetTotal.setText(txtTotal.getText());

                        }
                    }
                }
            }
            String date = loadDate();
            String month = loadMonth();
            String checkDate = new DiscountController().getDayDiscount(date);
            String checkMonth = new DiscountController().getMothDiscount(month);
            if (checkDate != null) {
                double discount = Double.parseDouble(lblDiscount.getText());
                double total = Double.parseDouble(txtTotal.getText());
                double cashDisc = total * discount / 100;
                double netTotal = total * (100 - discount) / 100;
                txtNetTotal.setText(Double.toString(netTotal));
                txtDayDiscount.setText(Double.toString(cashDisc));

            } else if (checkMonth != null) {
                double discount = Double.parseDouble(lblDiscount.getText());
                double total = Double.parseDouble(txtTotal.getText());
                double netTotal = total * (100 - discount) / 100;
                txtNetTotal.setText(Double.toString(netTotal));
                double cashDisc = total * discount / 100;
                txtDayDiscount.setText(Double.toString(cashDisc));
            }

            String discount = new DiscountController().getCardDiscount();
            if (discount != null) {
                double value = Double.parseDouble(discount);
                if (value != 0) {
                    double total = Double.parseDouble(txtTotal.getText());
                    double netTotal = total * (100 - value) / 100;
                    txtNetTotal.setText(Double.toString(netTotal));
                    double cardDiscount = total * value / 100;
                    txtCardDiscount.setText(Double.toString(cardDiscount));
                }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PayBill.class.getName()).log(Level.SEVERE, null, ex);

        } catch (SQLException ex) {
            Logger.getLogger(PayBill.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_txtTotalKeyReleased

