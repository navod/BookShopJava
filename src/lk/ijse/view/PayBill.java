    private void txtQtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtyKeyReleased
        try {
            String pName = lblPName.getText();
            String discount = new ItemController().getProduct(pName);
            txtDiscount.setText(discount);

            if (txtDiscount != null) {
                double unitPrice = Double.parseDouble(txtUnitPrice.getText());
                double discount2 = Double.parseDouble(txtDiscount.getText());
                double total = (unitPrice * (100 - discount2)) / 100;
                txtOPrice.setText(Double.toString(total));

                double unitPrice2 = Double.parseDouble(txtOPrice.getText());
                int qty = Integer.parseInt(txtQty.getText());
                double total1 = unitPrice2 * qty;
                txtAmount.setText(Double.toString(total1));
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PayBill.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PayBill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtQtyKeyReleased

