    private void btnAddToCart1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCart1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
        int qty = Integer.parseInt(txtQty.getText());

        int rowIndex = isAlreadyExsits(comboDiscription.getSelectedItem().toString());
        if (rowIndex == -1) {
            Object[] rowData = {
                comboDiscription.getSelectedItem().toString(),
                qty

            };
            model.addRow(rowData);
        } else {
            qty += (int) model.getValueAt(rowIndex, 1);

            tbl1.setValueAt(qty, rowIndex, 1);
        }

    }//GEN-LAST:event_btnAddToCart1ActionPerformed

    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        try {
            String orderId = lblOrderId.getText();
            String userId = lblOrderId2.getText();
            String custId = lblCustName.getText();
            String date = "date";
            
            ArrayList <OrderDetail> list = new ArrayList<>();
            DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                String itemId = lblItemId.getText();
                int qty = (int) model.getValueAt(i, 1);
                OrderDetail orderDetail = new OrderDetail(orderId, itemId, qty);
                list.add(orderDetail);
            }
            Order order = new Order(orderId,userId,custId,date,list);
            
            boolean isSaved = new OrderController().placeOrder(order);
            
            if (isSaved) {
                JOptionPane.showMessageDialog(this, "Order Placed");
                getLastId();
                
                tbl1.removeAll();
            } else {
                JOptionPane.showMessageDialog(this, "error");
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(OrderPanel.class.getName()).log(Level.SEVERE, null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(OrderPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

    private void tblordersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblordersMouseClicked

        try {
            
            int i = tblorders.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tblorders.getModel();
            lblOrder.setText(model.getValueAt(i, 0).toString());
            
            DefaultTableModel model1 = (DefaultTableModel) tblItemDetail.getModel();
            for(OrderDetail detail : new OrderDetailController().getItems(lblOrder.getText())){
                
               model1.addRow(new Object[]{
                detail.getOrderId(),
                detail.getItemId(),
                detail.getQty()
            });
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(OrderPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(OrderPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_tblordersMouseClicked
    public int isAlreadyExsits(String name) {
        DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String temp = model.getValueAt(i, 0).toString();
            if (temp.equalsIgnoreCase(name)) {
                return i;
            }
        }
        return -1;
    }

