    private void loadContact() throws ClassNotFoundException, SQLException {
        for (String temp : new CustomerController().getAllContact()) {
            comboCustContact.addItem(temp);
        }
    }

    private void loadDescription() throws ClassNotFoundException, SQLException {
        for (String temp : new ItemController().getDescription()) {
            comboDiscription.addItem(temp);
        }
    }

    private void getLastId() throws ClassNotFoundException, SQLException {
        String id = new OrderController().getLastOrderId();
        if (id == null) {
            lblOrderId.setText("OI0001");
        } else {
            String split[] = id.split("OI");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                lblOrderId.setText("OI000" + num);
            } else if (num < 100) {
                lblOrderId.setText("OI00" + num);
            } else if (num < 1000) {
                lblOrderId.setText("OI0" + num);
            } else {
                lblOrderId.setText("OI" + num);
            }
        }

    }

    private void loadAlOrders() throws ClassNotFoundException, SQLException {
        DefaultTableModel model = (DefaultTableModel) tblorders.getModel();
        for(Order order : new OrderController().getAllOrders()){
            model.addRow(new Object[]{
                order.getOrderId(),
                order.getUserId(),
                order.getDate()
            });
        }
    }

