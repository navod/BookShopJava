    private void loadCustomerId() throws ClassNotFoundException, SQLException {
        String id = new CustomerController().getLastId();
        if (id == null) {
            txtCustId.setText("C0001");
        } else {
            String split[] = id.split("C");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                txtCustId.setText("C000" + num);
            } else if (num < 100) {
                txtCustId.setText("C00" + num);
            } else if (num < 1000) {
                txtCustId.setText("C0" + num);
            } else {
                txtCustId.setText("C" + num);
            }
        }
    }
