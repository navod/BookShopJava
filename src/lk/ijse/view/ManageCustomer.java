    private void btnCustAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustAddActionPerformed

        try {
            String id = txtCustId.getText();
            String name = txtCustomerName.getText();
            String nic = txtCustIdCard.getText();
            String city = txtCustCity.getText();
            String line1 = txtAddress1.getText();
            String line2 = txtAddress2.getText();
            int contact = Integer.parseInt(txtCustomerContact.getText());
            double regfee = Double.parseDouble(txtRegfee.getText());
            double point = 0;
            boolean isSaved = new CustomerController().addedCustomer(new Customer(id, name, nic, city, contact, line1, line2, regfee, point));

            if (isSaved) {
                JOptionPane.showMessageDialog(this, "Saved");
                clear();
                loadCustomerId();

            } else {
                JOptionPane.showMessageDialog(this, "check again");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnCustAddActionPerformed
