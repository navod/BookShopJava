    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int i = tbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tbl.getModel();
        txtCustId.setText(model.getValueAt(i, 0).toString());
        txtCustName.setText(model.getValueAt(i, 1).toString());
        txtAddress1.setText(model.getValueAt(i, 4).toString());
        txtAddress2.setText(model.getValueAt(i, 5).toString());
        txtCustCity.setText(model.getValueAt(i, 6).toString());
        txtCustIdCard.setText(model.getValueAt(i, 2).toString());
        txtCustomerContact.setText(model.getValueAt(i, 3).toString());
        
        
        
    }//GEN-LAST:event_tblMouseClicked

