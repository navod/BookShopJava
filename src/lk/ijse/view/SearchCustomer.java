    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        
        
        try {
            String name = txtCustName.getText();
            String nic = txtCustIdCard.getText();
            int contact = Integer.parseInt(txtCustomerContact.getText());
            String address1 = txtAddress1.getText();
            String address2 = txtAddress2.getText();
            String city = txtCustCity.getText();
            String id = txtCustId.getText();
            
            Object isUpdate = new CustomerController().updateCustomer(name, nic, contact, city, address1, address2,id);
            if (isUpdate != null){
                JOptionPane.showMessageDialog(this,"Updated");
            }else{
                JOptionPane.showMessageDialog(this,"Cant Update");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

