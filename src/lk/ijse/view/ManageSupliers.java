    private void loadAllSupliers() throws ClassNotFoundException, SQLException {
       DefaultTableModel model = (DefaultTableModel)tbl.getModel();
       for(Supliers temp : new SuplierController().getAllSupliers()){
           model.addRow(new Object[]{
               temp.getSupId(),
               temp.getSupName(),
               temp.getAddress(),
               temp.getContact()
           });
       }
    }
