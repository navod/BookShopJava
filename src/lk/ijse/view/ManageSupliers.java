    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int i = tbl.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        txtSupId.setText(model.getValueAt(i, 0).toString());
        txtSupName.setText(model.getValueAt(i, 1).toString());
        txtSupAddress.setText(model.getValueAt(i, 2).toString());
        txtSupContact.setText(model.getValueAt(i, 3).toString());
    }//GEN-LAST:event_tblMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String id = txtSupId.getText();
            String name = txtSupName.getText();
            int contact = Integer.parseInt(txtSupContact.getText());
            String addrss = txtSupAddress.getText();
            
            boolean isUpate = new SuplierController().updateSuplier(new Supliers(id, name, addrss, contact));
            if(isUpate){
                JOptionPane.showMessageDialog(null, "Updated");
            }else{
                JOptionPane.showMessageDialog(null, "Try again");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageSupliers.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageSupliers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        try {
            DefaultTableModel model = (DefaultTableModel)tbl.getModel();
            model.setRowCount(0);
            
            for(Supliers temp : new SuplierController().searchSuplier(txtSearch.getText())){
                model.addRow(new Object[]{
                    temp.getSupId(),
                    temp.getSupName(),
                    temp.getAddress(),
                    temp.getContact()
                        
                });
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageSupliers.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageSupliers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

