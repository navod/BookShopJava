    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            //        try {
            String suplierId = txtSupId.getText();
            String supName = txtSupName.getText();
            String supAddress = txtSupAddress.getText();
            int contact = Integer.parseInt(txtSuoContact.getText());
            String itemId = txtItemId.getText();
            String description = txtDiscripton.getText();
            String status = getStatus();
            int totalQty = Integer.parseInt(txtQty.getText());
            String product = txtPname.getText();
            String author = txtAuthorName.getText();
            int rackNo = Integer.parseInt(txtRackNo.getText());
            int rowNo = Integer.parseInt(txtRowNo.getText());
            String barcode = txtBarcodeId.getText();
            double unitPrice = Double.parseDouble(txtUnitPrice.getText());
            double fulltotal = Double.parseDouble(txtTotal.getText());

            Object isSaved = new ItemController().addedNewItems(new Items(itemId, description, rackNo, rowNo, totalQty, unitPrice, status, product, author, barcode),
                    new Supliers(suplierId, supName, supAddress, contact),
                    fulltotal
            );
            if (isSaved != null) {
                JOptionPane.showMessageDialog(this, "Saved");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddsupandItem.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddsupandItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

