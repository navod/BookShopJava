    private String getStatus() {
        String status = "";
        if (rSchoolStationery.isSelected()) {
            status = "staioney";

        } else if (rOthers.isSelected()) {
            status = "Others";

        }
        return status;
    }

    private void loadSuplierId() throws ClassNotFoundException, SQLException {
        String id = new SuplierController().getLastId();
        if (id == null) {
            txtSupId.setText("S0001");
        } else {
            String split[] = id.split("S");

            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                txtSupId.setText("S000" + num);
            } else if (num < 100) {
                txtSupId.setText("S00" + num);
            } else if (num < 1000) {
                txtSupId.setText("S0" + num);
            } else {
                txtSupId.setText("S" + num);
            }

        }
    }

    private void loadItemId() throws ClassNotFoundException, SQLException {
        String id = new ItemController().getLastId();
        if (id == null) {
            txtItemId.setText("T0001");
        } else {
            String split[] = id.split("T");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                txtItemId.setText("T000" + num);
            } else if (num < 100) {
                txtItemId.setText("T00" + num);
            } else if (num < 1000) {
                txtItemId.setText("T0" + num);
            } else {
                txtItemId.setText("T" + num);
            }
        }
    }
